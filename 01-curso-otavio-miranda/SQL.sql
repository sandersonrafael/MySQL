# Comandos SQL

* DDL - Data Definition Language

-> CREATE
-> ALTER
-> DROP
-> RENAME
-> TRUNCATE
-> COMMENT

* DQL - Data Query Language

-> SELECT

* DML - Data Manipulation Language

-> INSERT
-> UPDATE
-> DELETE
-> MERGE
-> CALL
-> EXPLAIN PLAIN
-> LOCK TABLE

* DCL - Data Control Language

-> GRANT
-> REVOKE

*** Os principais para desenvolvimento (CRUD), são os DQL e DML