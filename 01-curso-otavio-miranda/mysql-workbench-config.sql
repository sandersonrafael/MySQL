# Instalar MySQL Workbench Community

* Fazer download e instalação no site oficial
* Executar o docker conforme arquivo docker-mysql-config
* Conectar com o IP do computador (no ubuntu, buscar pelo comando ip address)
* Usuário por padrão no docker é o root
* Usar porta definida ao iniciar o docker (3306, por padrão)
* Usar senha informada ao iniciar o container docker (senha123 por exemplo)
